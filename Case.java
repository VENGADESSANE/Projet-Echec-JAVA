
public class Case {
	private int x;
	private int y;
	
	//il prend les coordonnées dans l'échiquier
	public Case(int x, int y) {
		this.x = x;
		this.y = y;
	}

	/* GETTERS & SETTERS */
	public int getX() {
		return this.x;
	}


	public void setX(int x) {
		this.x = x;
	}


	public int getY() {
		return this.y;
	}


	public void setY(int y) {
		this.y = y;
	}


	/* FIN GETTERS & SETTERS */
	

}
